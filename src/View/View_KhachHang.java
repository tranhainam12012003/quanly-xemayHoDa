    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        int row = tblKhachHang.getSelectedRow();
        txtMaKhachHang.setText(tblKhachHang.getValueAt(row, 1).toString());
        txtHoVaTen.setText(tblKhachHang.getValueAt(row, 2).toString());
        txtDiaChi.setText(tblKhachHang.getValueAt(row, 3).toString());
        if (tblKhachHang.getValueAt(row, 4).equals("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        txtSDT.setText(tblKhachHang.getValueAt(row, 5).toString());
    }//GEN-LAST:event_tblKhachHangMouseClicked


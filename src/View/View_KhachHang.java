    private void btnThemPanelKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemPanelKhachHangActionPerformed
        D_KhachHang khachhang = getForm();
        if (khachhang == null) {
            return;
        }
        if (rdoNam.isSelected()) {
            khachhang.setGioiTinh(0);
        } else if (rdoNu.isSelected()) {
            khachhang.setGioiTinh(1);
        }
        if (khachhangService.insert(khachhang)) {
            fillToTableKhachHang(khachhangService.getAll());
            clearForm();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            //            qlkh.loadKH(S_ctsp.getAllKH());
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại sdt hoặc mã khách hàng đã tồn tại ");
        }
    }//GEN-LAST:event_btnThemPanelKhachHangActionPerformed

    private void btnSuaPanelKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaPanelKhachHangActionPerformed
        if (txtMaKhachHang.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Chưa chọn khách hàng");
            return;
        }
        D_KhachHang khachhang = getForm();
        if (khachhang == null) {
            return;
        }
        if (rdoNam.isSelected()) {
            khachhang.setGioiTinh(0);
        } else if (rdoNu.isSelected()) {
            khachhang.setGioiTinh(1);
        }
        if (khachhangService.update(txtMaKhachHang.getText(), khachhang)) {
            fillToTableKhachHang(khachhangService.getAll());
            clearForm();
            JOptionPane.showMessageDialog(this, "Cập nhật thánh công");
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại sdt hoặc mã khách hàng đã tồn tại");
        }
    }//GEN-LAST:event_btnSuaPanelKhachHangActionPerformed


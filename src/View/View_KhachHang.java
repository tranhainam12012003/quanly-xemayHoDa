    private void fillToTableKhachHang(List<D_KhachHang> list) {
        khachHangModelTB = (DefaultTableModel) tblKhachHang.getModel();
        khachHangModelTB.setRowCount(0);
        int count = 1;
        for (D_KhachHang khachhang : list) {
            khachHangModelTB.addRow(new Object[]{
                count++,
                //                khachhang.getIdKH(),
                khachhang.getMaKH(),
                khachhang.getHoTen(),
                khachhang.getDiaChi(),
                getGioiTinh(khachhang.getGioiTinh()),
                khachhang.getSdt()
            });
        }
    }

    public String getGioiTinh(int gioiTinh) {
        if (gioiTinh == 0) {
            return "Nam";
        } else if (gioiTinh == 1) {
            return "Nữ";
        } else {
            return null;
        }
    }


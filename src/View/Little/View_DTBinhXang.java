    public boolean checkForm() {

        String dungTichText = txtXL.getText().trim();
//        int dt = Integer.parseInt(txtXL.getText());

        if (dungTichText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống Dung Tích Bình Xăng");
            return false;
        }

        try {
            double dt = Double.parseDouble(dungTichText);
            // Đã chuyển đổi thành công thành số nguyên
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Không hợp lệ , vui lòng thử lại");
            return false;
        }

        // Dung Tích Bình Xăng là số nguyên hợp lệ
        return true;
    }


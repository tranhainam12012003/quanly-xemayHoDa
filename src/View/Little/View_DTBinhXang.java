    public void LoadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tlbBang.getModel();
        dtm.setRowCount(0);
        int stt = 1;
        for (D_DTBinhXang x : XiLanh.getAllBX()) {
            dtm.addRow(new Object[]{
                stt,
                x.getId(),
                x.getDTBinhXang() + " Lít"

            });
            stt++;
        }
    }

